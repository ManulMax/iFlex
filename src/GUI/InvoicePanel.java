    public boolean doubleValidation(JTextField tf) {

        try {
            Double.parseDouble(tf.getText());
            return true;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Invalid value", "Error", JOptionPane.ERROR_MESSAGE);
            tf.grabFocus();
            return false;
        }

    }

    public void calculateNetAmount() {

        double netTotal = 0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {

            double invoTotal = Double.parseDouble(jTable1.getValueAt(i, 6).toString());

            netTotal += invoTotal;
        }

        jLabel10.setText(String.valueOf(netTotal));

    }

