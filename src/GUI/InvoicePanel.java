    public void loadNextInvoiceId() {

        ResultSet r;
        try {
            r = DB.search("SELECT idInvoice FROM invoice");
            ArrayList<String> idInvoices = new ArrayList<>();

            while (r.next()) {
                idInvoices.add(r.getString("idInvoice"));

            }

            if (idInvoices.size() > 0) {

                ArrayList<Integer> ids = new ArrayList();

                for (String idinvoice : idInvoices) {

                    ids.add(Integer.parseInt(idinvoice.split("INV")[1]));
                }

                Collections.sort(ids);
                int newInvID = ids.get(ids.size() - 1) + 1;
                String ID = ("INV" + newInvID);
                jLabel2.setText(ID);

            } else {

                jLabel2.setText("INV1");

            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

