    private void loadUser() {
        try {
            ResultSet res = DB.search("select * from user where userid ='"+ userid +"'");

            while (res.next()) {
                jTextField1.setText(res.getString(1));
                jTextField2.setText(res.getString(2));
                jTextField3.setText(res.getString(3));
                jTextField4.setText(res.getString(8));
                jTextField5.setText(res.getString(4));
                jTextField6.setText(res.getString(7));
                jTextField7.setText(res.getString(5));
                
                String gender = res.getString("gender");
                if (gender.equalsIgnoreCase("male")) {
                    jRadioButton3.setSelected(true);
                } else {
                    jRadioButton1.setSelected(true);
                }
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

