    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String userid = jTextField1.getText();
            String fname = jTextField2.getText();
            String lname = jTextField3.getText();
            String mobile = jTextField4.getText();
            String email = jTextField5.getText();
            String address = jTextField6.getText();
            String password = jTextField7.getText();
            String gender = "";

            if (jRadioButton3.isSelected()) {
                gender = "male";
            } else {
                gender = "female";
            }
            if (userid.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter UserID");
            } else if (fname.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter First Name");
            } else if (lname.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Last Name");
            } else if (mobile.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Telephone Number");
            } else if (email.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Email");
            } else if (address.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Address");
            } else if (password.equals("")) {
                JOptionPane.showMessageDialog(this, "Enter Password");
            } else if (gender.equals("")) {
                JOptionPane.showMessageDialog(this, "Select Gender");
            } else if (mobile.length() != 10) {
                JOptionPane.showMessageDialog(this, "Invalid Telephone Number");
            } else {
                DB.iud("update user set userid = '" + userid + "', fname = '" + fname + "', lname = '" + lname + "', mobile = '" + mobile + "' ,email = '" + email + "', address = '" + address + "' ,password = '" + password + "' ,gender = '" + gender + "' ");
                JOptionPane.showMessageDialog(this, "Successfully Updated");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

